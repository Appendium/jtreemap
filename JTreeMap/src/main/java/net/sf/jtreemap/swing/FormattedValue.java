package net.sf.jtreemap.swing;

import java.math.BigDecimal;
import java.text.NumberFormat;

/**
 * class who can display the values of elements of a JTreeMap with a formatter.
 *
 * @author Benoit Xhenseval
 */

public class FormattedValue extends DefaultValue {
    private static final long serialVersionUID = 108728719010392928L;
    private final NumberFormat nf;

    public FormattedValue() {
        this(null);
    }

    public FormattedValue(final NumberFormat nf) {
        this(nf, null);
    }

    public FormattedValue(final NumberFormat nf, final double value) {
        super(value);
        this.nf = nf;
    }

    public FormattedValue(final NumberFormat nf, final BigDecimal value) {
        super(value != null ? value.doubleValue() : 0.0);
        this.nf = nf;
    }

    @Override
    public String getLabel() {
        return nf != null ? nf.format(getValue()) : String.valueOf(getValue());
    }
}
